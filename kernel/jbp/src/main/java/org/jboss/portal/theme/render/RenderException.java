package org.jboss.portal.theme.render;

/**
 * Generic exception for the render process.
 *
 * @author <a href="mailto:mholzner@novell.com>Martin Holzner</a>
 * @version $LastChangedRevision: 8784 $, $LastChangedDate: 2007-10-27 19:01:46 -0400 (Sat, 27 Oct 2007) $
 */
public class RenderException
   extends Exception
{
   /** The serialVersionUID */
   private static final long serialVersionUID = 6960212365830368099L;

   /** @see java.lang.Exception */
   public RenderException()
   {
   }

   /** @see java.lang.Exception */
   public RenderException(Throwable t)
   {
      super(t);
   }

   /** @see java.lang.Exception */
   public RenderException(String message)
   {
      super(message);
   }
}
